const makeFont = (data: Record<string, string[]>) => {
    const font: Record<string, number[][]> = {};
    for (const [char, rows] of Object.entries(data)) {
        font[char] = rows.map(row => row.split('').map(c => (c === '#' ? 1 : 0)));
    }
    return font;
};

